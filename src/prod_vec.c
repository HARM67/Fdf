#include "fdf.h"

t_vec4	prod_vec(t_vec4 a, t_vec4 b)
{
	t_vec4 rt;

	rt.x = a.y * b.z - b.y * a.z;
	rt.y = a.z * b.x - b.z * a.x;
	rt.z = a.x * b.y - b.x * a.y;
	return (rt);
}
